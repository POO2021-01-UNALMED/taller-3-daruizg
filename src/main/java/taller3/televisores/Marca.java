package taller3.televisores;

public class Marca {
	
	// ATRIBUTOS
	private String nombre;
	
	// MÉTODOS
	// Constructor
	public Marca(String nombre) {
		this.nombre = nombre;
	}
	
	// Setters y Getters
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	
	public String getNombre() {
		return nombre;
	}
}
